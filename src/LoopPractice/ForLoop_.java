package LoopPractice;

public class ForLoop_ {
    public static void main(String[] args) {

        String []fruits = new String[5];
        fruits [0] = "a";
        fruits [1] = "b";
        fruits [2] = "c";
        fruits [3] = "d";
        fruits [4] = "e";

        for (int i =0;i <=5;i++) {
            System.out.println(fruits[i]);
        }


    }
}
